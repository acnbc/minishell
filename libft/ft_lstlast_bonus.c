#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*curr;

	if (!lst)
		return (NULL);
	curr = lst;
	while (curr != NULL)
	{
		if (curr->next == NULL)
			break ;
		curr = curr->next;
	}
	return (curr);
}
